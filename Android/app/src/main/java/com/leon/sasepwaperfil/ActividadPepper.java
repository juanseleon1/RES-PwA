package com.leon.sasepwaperfil;

public class ActividadPepper {
    private String tipo;
    private double duracion;
    private Dificultad dificultad;
    private float gusto;
    private int enriquecimiento;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getDuracion() {
        return duracion;
    }

    public void setDuracion(double duracion) {
        this.duracion = duracion;
    }

    public Dificultad getDificultad() {
        return dificultad;
    }

    public void setDificultad(Dificultad dificultad) {
        this.dificultad = dificultad;
    }

    public float getGusto() {
        return gusto;
    }

    public void setGusto(float gusto) {
        this.gusto = gusto;
    }

    public int getEnriquecimiento() {
        return enriquecimiento;
    }

    public void setEnriquecimiento(int enriquecimiento) {
        this.enriquecimiento = enriquecimiento;
    }
}
